function calculateFunctionResult(formula, domains) {
  const results = [];
  for (let i = 0; i < domains.length; i++) {
    const x = domains[i];
    const result = evaluateFormula(formula, x); 
    results.push(`f(${x}) = ${result}`);
  }
  return results;
}

function evaluateFormula(formula, x) {
  const parts = formula.split('=');
  if (parts.length !== 2) {
    throw new Error("Invalid formula");
  }

  // Tidak perlu mengganti 'x' dengan nilai x di sini

  const expression = parts[1].trim();
  
  const replacedExpression = expression.replace(/\^/g, '**');

  const multiplicationExpression = replacedExpression.replace(/(\d+)x/g, '$1*x');

  // Tidak perlu mengganti 'x' dengan nilai x di sini

  return eval(multiplicationExpression.replace(/ /g, ""));
}

